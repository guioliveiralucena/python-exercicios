num = int(input('Digite um número inteiro: '))

# Faz as contas com a variável
dobro = num / 2
triplo = num * 3
raizquadrada = num ** (1/2)

# Mostra na tela os resultados
print('O dobro/metade desse número é {}'.format(dobro))
print('O triplo desse número é {}'.format(triplo))
print('E a raiz quadrada é {}'.format(raizquadrada))