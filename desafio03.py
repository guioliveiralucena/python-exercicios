num = int(input(' Digite um número inteiro: '))

# Descobre o antecessor e o sucessor de um número.
antecessor = num - 1
sucessor = num + 1

# Printa na tela o a frase e o sucessor e antecessor.
print('\n O sucessor desse número é {}'.format(sucessor))
print(' E o antecessor desse número é {}'.format(antecessor))
